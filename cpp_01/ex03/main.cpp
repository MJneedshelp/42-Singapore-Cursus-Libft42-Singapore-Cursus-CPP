#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"


int	main(void)
{
	{
		std::cout << "==========Test 1: Reference==========" << std::endl;
		Weapon club = Weapon("crude spiked club");
		HumanA bob("Bob", club);

		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		std::cout << "===========Test 2: Pointer===========" << std::endl;
		Weapon club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}



	return (0);
}

